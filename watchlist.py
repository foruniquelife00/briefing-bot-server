def load_watchlist() -> list:
    if os.path.exists(WATCHLIST_FILE):
        with open(WATCHLIST_FILE, "r", encoding="utf-8") as f:
            return json.load(f)
    return DEFAULT_WATCHLIST.copy()

def save_watchlist(watchlist: list):
    with open(WATCHLIST_FILE, "w", encoding="utf-8") as f:
        json.dump(watchlist, f, ensure_ascii=False, indent=2)

def add_stock(name: str) -> str:
    if name not in STOCK_MAP:
        return f"❌ '{name}' 는 지원하지 않는 종목이에요.\n/종목목록 으로 확인해보세요."
    watchlist = load_watchlist()
    if name in watchlist:
        return f"⚠️ '{name}' 는 이미 워치리스트에 있어요."
    watchlist.append(name)
    save_watchlist(watchlist)
    return f"✅ '{name}' 를 워치리스트에 추가했어요!"

def remove_stock(name: str) -> str:
    watchlist = load_watchlist()
    if name not in watchlist:
        return f"⚠️ '{name}' 는 워치리스트에 없어요."
    watchlist.remove(name)
    save_watchlist(watchlist)
    return f"✅ '{name}' 를 워치리스트에서 삭제했어요."

