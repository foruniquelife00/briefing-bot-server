def get_stock_price(name: str) -> str:
    if name not in STOCK_MAP:
        return f"❌ '{name}' 는 지원하지 않는 종목이에요."
    ticker = STOCK_MAP[name]
    try:
        t      = yf.Ticker(ticker)
        info   = t.fast_info
        price  = info.last_price
        prev   = info.regular_market_previous_close
        change = price - prev
        rate   = (change / prev) * 100
        high52 = info.year_high
        low52  = info.year_low
        is_kr  = ticker.endswith(".KS") or ticker.endswith(".KQ")
        arrow  = "▲" if rate >= 0 else "▼"

        if is_kr:
            price_str  = f"{int(price):,}원"
            change_str = f"{int(change):+,}원"
            high_str   = f"{int(high52):,}원"
            low_str    = f"{int(low52):,}원"
        else:
            price_str  = f"${price:.2f}"
            change_str = f"${change:+.2f}"
            high_str   = f"${high52:.2f}"
            low_str    = f"${low52:.2f}"

        return (
            f"📊 {name} ({ticker})\n"
            f"현재가: {price_str}\n"
            f"등락: {arrow} {change_str} ({rate:+.2f}%)\n"
            f"52주 고가: {high_str}\n"
            f"52주 저가: {low_str}"
        )
    except Exception as e:
        return f"❌ '{name}' 시세 조회 실패: {e}"

